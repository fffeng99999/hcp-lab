#!/bin/bash
set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/../../.." && pwd)"
REPORT_OUT="$SCRIPT_DIR/report"

NODES_LIST="${NODES_LIST:-16,32,64,128,256}"
TARGET_TPS_LIST="${TARGET_TPS_LIST:-1000,2000,3000}"
TX_TOTAL="${TX_TOTAL:-100,1000,10000}"
REPEAT="${REPEAT:-5}"
FAULTY_RATIO_LIST="${FAULTY_RATIO_LIST:-0,0.1,0.2}"

PORT_OFFSET="${PORT_OFFSET:-9000}"
CHAIN_ID="${CHAIN_ID:-hcp-exp9}"

HOTSTUFF_NODE_COUNT="${HOTSTUFF_NODE_COUNT:-4}"
HOTSTUFF_FAULTY_RATIO="${HOTSTUFF_FAULTY_RATIO:-0}"
HOTSTUFF_VIEW_TIMEOUT_MS="${HOTSTUFF_VIEW_TIMEOUT_MS:-5000}"
HOTSTUFF_TIMEOUT_EXPONENT="${HOTSTUFF_TIMEOUT_EXPONENT:-2.0}"
HOTSTUFF_BASE_LATENCY_MS="${HOTSTUFF_BASE_LATENCY_MS:-1}"
HOTSTUFF_JITTER_MS="${HOTSTUFF_JITTER_MS:-0.5}"
HOTSTUFF_MESSAGE_BYTES="${HOTSTUFF_MESSAGE_BYTES:-256}"
HOTSTUFF_PIPELINE_DEPTH="${HOTSTUFF_PIPELINE_DEPTH:-3}"
HOTSTUFF_ENABLE_THRESHOLD_SIG="${HOTSTUFF_ENABLE_THRESHOLD_SIG:-false}"

export PORT_OFFSET
export CHAIN_ID
export HOTSTUFF_NODE_COUNT
export HOTSTUFF_FAULTY_RATIO
export HOTSTUFF_VIEW_TIMEOUT_MS
export HOTSTUFF_TIMEOUT_EXPONENT
export HOTSTUFF_BASE_LATENCY_MS
export HOTSTUFF_JITTER_MS
export HOTSTUFF_MESSAGE_BYTES
export HOTSTUFF_PIPELINE_DEPTH
export HOTSTUFF_ENABLE_THRESHOLD_SIG

echo "开始实验9：HotStuff 性能建模"
echo "节点列表: $NODES_LIST"
echo "目标TPS列表: $TARGET_TPS_LIST"
echo "总交易数: $TX_TOTAL"
echo "故障率列表: $FAULTY_RATIO_LIST"
echo "链ID: $CHAIN_ID"
echo "端口偏移: $PORT_OFFSET"
echo "HotStuff参数: NODE_COUNT=$HOTSTUFF_NODE_COUNT FAULTY_RATIO=$HOTSTUFF_FAULTY_RATIO"
echo "HotStuff参数: VIEW_TIMEOUT_MS=$HOTSTUFF_VIEW_TIMEOUT_MS TIMEOUT_EXPONENT=$HOTSTUFF_TIMEOUT_EXPONENT"
echo "HotStuff参数: BASE_LATENCY_MS=$HOTSTUFF_BASE_LATENCY_MS JITTER_MS=$HOTSTUFF_JITTER_MS"
echo "HotStuff参数: MESSAGE_BYTES=$HOTSTUFF_MESSAGE_BYTES PIPELINE_DEPTH=$HOTSTUFF_PIPELINE_DEPTH"
echo "共识引擎: hotstuff (数学模型)"

mkdir -p "$REPORT_OUT"

cd "$PROJECT_ROOT/hcp-lab"
export PYTHONPATH="$PROJECT_ROOT/hcp-lab:${PYTHONPATH}"

python3 "$SCRIPT_DIR/run_exp9.py" \
  --nodes "$NODES_LIST" \
  --tps "$TARGET_TPS_LIST" \
  --tx "$TX_TOTAL" \
  --faulty-ratio "$FAULTY_RATIO_LIST" \
  --base-latency-ms "$HOTSTUFF_BASE_LATENCY_MS" \
  --jitter-ms "$HOTSTUFF_JITTER_MS" \
  --pipeline-depth "$HOTSTUFF_PIPELINE_DEPTH" \
  --message-bytes "$HOTSTUFF_MESSAGE_BYTES" \
  --view-timeout-ms "$HOTSTUFF_VIEW_TIMEOUT_MS" \
  --timeout-exponent "$HOTSTUFF_TIMEOUT_EXPONENT" \
  --out "$REPORT_OUT"

echo "实验9完成"
