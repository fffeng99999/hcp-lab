#!/bin/bash
set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/../../.." && pwd)"
REPORT_OUT="$SCRIPT_DIR/report"

NODES_LIST="${NODES_LIST:-4,8,16,32}"
TARGET_TPS_LIST="${TARGET_TPS_LIST:-1000,2000,3000}"
TX_TOTAL="${TX_TOTAL:-3000}"

PORT_OFFSET="${PORT_OFFSET:-10000}"
CHAIN_ID="${CHAIN_ID:-hcp-exp10}"

RAFT_NODE_COUNT="${RAFT_NODE_COUNT:-4}"
RAFT_ELECTION_TIMEOUT_MS="${RAFT_ELECTION_TIMEOUT_MS:-150}"
RAFT_HEARTBEAT_INTERVAL_MS="${RAFT_HEARTBEAT_INTERVAL_MS:-50}"
RAFT_ELECTION_TIMEOUT_RANGE_MS="${RAFT_ELECTION_TIMEOUT_RANGE_MS:-150}"
RAFT_SNAPSHOT_DISTANCE="${RAFT_SNAPSHOT_DISTANCE:-10000}"
RAFT_MAX_LOG_ENTRIES_PER_RPC="${RAFT_MAX_LOG_ENTRIES_PER_RPC:-500}"
RAFT_MESSAGE_BYTES="${RAFT_MESSAGE_BYTES:-256}"
RAFT_FAULTY_RATIO="${RAFT_FAULTY_RATIO:-0}"
RAFT_BASE_LATENCY_MS="${RAFT_BASE_LATENCY_MS:-1.0}"
RAFT_JITTER_MS="${RAFT_JITTER_MS:-0.5}"

export PORT_OFFSET
export CHAIN_ID
export RAFT_NODE_COUNT
export RAFT_ELECTION_TIMEOUT_MS
export RAFT_HEARTBEAT_INTERVAL_MS
export RAFT_ELECTION_TIMEOUT_RANGE_MS
export RAFT_SNAPSHOT_DISTANCE
export RAFT_MAX_LOG_ENTRIES_PER_RPC
export RAFT_MESSAGE_BYTES
export RAFT_FAULTY_RATIO
export RAFT_BASE_LATENCY_MS
export RAFT_JITTER_MS

echo "开始实验10：Raft 性能建模"
echo "节点列表: $NODES_LIST"
echo "目标TPS列表: $TARGET_TPS_LIST"
echo "总交易数: $TX_TOTAL"
echo "链ID: $CHAIN_ID"
echo "端口偏移: $PORT_OFFSET"
echo "Raft参数: NODE_COUNT=$RAFT_NODE_COUNT ELECTION_TIMEOUT_MS=$RAFT_ELECTION_TIMEOUT_MS"
echo "Raft参数: HEARTBEAT_INTERVAL_MS=$RAFT_HEARTBEAT_INTERVAL_MS ELECTION_TIMEOUT_RANGE_MS=$RAFT_ELECTION_TIMEOUT_RANGE_MS"
echo "Raft参数: SNAPSHOT_DISTANCE=$RAFT_SNAPSHOT_DISTANCE MAX_LOG_ENTRIES_PER_RPC=$RAFT_MAX_LOG_ENTRIES_PER_RPC"
echo "Raft参数: MESSAGE_BYTES=$RAFT_MESSAGE_BYTES FAULTY_RATIO=$RAFT_FAULTY_RATIO"
echo "Raft参数: BASE_LATENCY_MS=$RAFT_BASE_LATENCY_MS JITTER_MS=$RAFT_JITTER_MS"
echo "共识引擎: raft (数学模型)"

mkdir -p "$REPORT_OUT"

cd "$PROJECT_ROOT/hcp-lab"
export PYTHONPATH="$PROJECT_ROOT/hcp-lab:${PYTHONPATH}"

python3 "$SCRIPT_DIR/run_exp10.py" \
  --nodes "$NODES_LIST" \
  --tps "$TARGET_TPS_LIST" \
  --tx "$TX_TOTAL" \
  --election-timeout-ms "$RAFT_ELECTION_TIMEOUT_MS" \
  --heartbeat-interval-ms "$RAFT_HEARTBEAT_INTERVAL_MS" \
  --election-timeout-range-ms "$RAFT_ELECTION_TIMEOUT_RANGE_MS" \
  --snapshot-distance "$RAFT_SNAPSHOT_DISTANCE" \
  --max-log-entries "$RAFT_MAX_LOG_ENTRIES_PER_RPC" \
  --message-bytes "$RAFT_MESSAGE_BYTES" \
  --faulty-ratio "$RAFT_FAULTY_RATIO" \
  --base-latency-ms "$RAFT_BASE_LATENCY_MS" \
  --jitter-ms "$RAFT_JITTER_MS" \
  --out "$REPORT_OUT"

echo "实验10完成"
